package thefirstchange.example.com.communicationtext.gson;

public class UserInGroupInfo {

	private int groupid;
	private String ph;
	private String groupnickname;
	private String auth;
	private String corpos;
	private String corppart;
	private String icon;


	public String getIcon() {
		return icon;
	}

	public void setIcon(String icon) {
		this.icon = icon;
	}

	public String getCorppart() {
		return corppart;
	}

	public void setCorppart(String corppart) {
		this.corppart = corppart;
	}

	public int getGroupid() {
		return groupid;
	}

	public void setGroupid(int groupid) {
		this.groupid = groupid;
	}

	public String getPh() {
		return ph;
	}
	public void setPh(String ph) {
		this.ph = ph;
	}
	public String getGroupnickname() {
		return groupnickname;
	}
	public void setGroupnickname(String groupnickname) {
		this.groupnickname = groupnickname;
	}
	public String getAuth() {
		return auth;
		
	}
	public void setAuth(String auth) {
		this.auth = auth;
	}
	public String getCorpos() {
		return corpos;
	}
	public void setCorpos(String corpos) {
		this.corpos = corpos;
	}
	
	
	
	
}
